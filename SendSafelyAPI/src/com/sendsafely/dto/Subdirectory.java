package com.sendsafely.dto;

import java.util.Date;

public class Subdirectory {

	private Date directoryCreatedDate;
	private String directoryName;
	private String directoryId;
	
	public Date getDirectoryCreatedDate() {
		return directoryCreatedDate;
	}
	public void setDirectoryCreatedDate(Date directoryCreatedDate) {
		this.directoryCreatedDate = directoryCreatedDate;
	}
	public String getDirectoryName() {
		return directoryName;
	}
	public void setDirectoryName(String directoryName) {
		this.directoryName = directoryName;
	}
	public String getDirectoryId() {
		return directoryId;
	}
	public void setDirectoryId(String directoryId) {
		this.directoryId = directoryId;
	}
	
	
	
}
